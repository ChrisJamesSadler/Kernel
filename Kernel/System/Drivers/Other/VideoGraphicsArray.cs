        public VideoGraphicsArray(PCIDevice host) : base(host)
        {
            Label = "Video Graphics Array";
            Settings.EnterTextMode = EnterTextMode;
            Settings.EnterVideoMode = EnterGraphicsMode;
        }

        void EnterTextMode()
        {
            WriteVGARegisters(graphicsRegisters);
            WriteVGARegisters(textRegisters);
            WriteFont(fontDate, 16);
            Settings.DisplayWidth = 80;
            Settings.DisplayHeight = 25;
        }

        void EnterGraphicsMode()
        {
            WriteVGARegisters(textRegisters);
            WriteFont(fontDate, 16);
            WriteVGARegisters(graphicsRegisters);
            Settings.DisplayVRam = (uint*)0xA0000;
            Settings.DisplayWidth = 320;
            Settings.DisplayHeight = 200;
        }

        private unsafe void WriteVGARegisters(byte[] registers)
        {
            fixed (byte* reg = registers)
            {
                byte* regs = reg;
                byte i;

                /* write MISCELLANEOUS reg */
                IOPort.outb(VGA_MISC_WRITE, *regs);
                regs++;
                /* write SEQUENCER regs */
                for (i = 0; i < VGA_NUM_SEQ_REGS; i++)
                {
                    IOPort.outb(VGA_SEQ_INDEX, i);
                    IOPort.outb(VGA_SEQ_DATA, *regs);
                    regs++;
                }
                /* unlock CRTC registers */
                IOPort.outb(VGA_CRTC_INDEX, 0x03);
                IOPort.outb(VGA_CRTC_DATA, (byte)(IOPort.inb(VGA_CRTC_DATA) | 0x80));
                IOPort.outb(VGA_CRTC_INDEX, 0x11);
                IOPort.outb(VGA_CRTC_DATA, (byte)(IOPort.inb(VGA_CRTC_DATA) & 0x7f));
                /* make sure they remain unlocked */
                regs[0x03] |= 0x80;
                regs[0x11] &= 0x7f;
                /* write CRTC regs */
                for (i = 0; i < VGA_NUM_CRTC_REGS; i++)
                {
                    IOPort.outb(VGA_CRTC_INDEX, i);
                    IOPort.outb(VGA_CRTC_DATA, *regs);
                    regs++;
                }
                /* write GRAPHICS CONTROLLER regs */
                for (i = 0; i < VGA_NUM_GC_REGS; i++)
                {
                    IOPort.outb(VGA_GC_INDEX, i);
                    IOPort.outb(VGA_GC_DATA, *regs);
                    regs++;
                }
                /* write ATTRIBUTE CONTROLLER regs */
                for (i = 0; i < VGA_NUM_AC_REGS; i++)
                {
                    IOPort.inb(VGA_INSTAT_READ);
                    IOPort.outb(VGA_AC_INDEX, i);
                    IOPort.outb(VGA_AC_WRITE, *regs);
                    regs++;
                }
                /* lock 16-color palette and unblank display */
                IOPort.inb(VGA_INSTAT_READ);
                IOPort.outb(VGA_AC_INDEX, 0x20);
            }
        }

        private unsafe void WriteFont(byte[] font, byte font_height)
        {
            byte seq2, seq4, gc4, gc5, gc6;

            /* save registers
        set_plane() modifies GC 4 and SEQ 2, so save them as well */
            IOPort.outb(VGA_SEQ_INDEX, 2);
            seq2 = IOPort.inb(VGA_SEQ_DATA);

            IOPort.outb(VGA_SEQ_INDEX, 4);
            seq4 = IOPort.inb(VGA_SEQ_DATA);
            /* turn off even-odd addressing (set flat addressing)
            assume: chain-4 addressing already off */
            IOPort.outb(VGA_SEQ_DATA, (byte)(seq4 | 0x04));

            IOPort.outb(VGA_GC_INDEX, 4);
            gc4 = IOPort.inb(VGA_GC_DATA);

            IOPort.outb(VGA_GC_INDEX, 5);
            gc5 = IOPort.inb(VGA_GC_DATA);
            /* turn off even-odd addressing */
            IOPort.outb(VGA_GC_DATA, (byte)(gc5 & ~0x10));

            IOPort.outb(VGA_GC_INDEX, 6);
            gc6 = IOPort.inb(VGA_GC_DATA);
            /* turn off even-odd addressing */
            IOPort.outb(VGA_GC_DATA, (byte)(gc6 & ~0x02));
            /* write font to plane P4 */
            set_plane(2);
            /* write font 0 */
            int seg = get_fb_seg();
            for (int i = 0; i < 256; i++)
                for (int j = 0; j < font_height; j++)
                    *((byte*)(seg + i * 32 + j)) = font[i * font_height + j];


            /* restore registers */
            IOPort.outb(VGA_SEQ_INDEX, 2);
            IOPort.outb(VGA_SEQ_DATA, seq2);
            IOPort.outb(VGA_SEQ_INDEX, 4);
            IOPort.outb(VGA_SEQ_DATA, seq4);
            IOPort.outb(VGA_GC_INDEX, 4);
            IOPort.outb(VGA_GC_DATA, gc4);
            IOPort.outb(VGA_GC_INDEX, 5);
            IOPort.outb(VGA_GC_DATA, gc5);
            IOPort.outb(VGA_GC_INDEX, 6);
            IOPort.outb(VGA_GC_DATA, gc6);
        }

        private void set_plane(byte p)
        {
            byte pmask;

            p &= 3;
            pmask = (byte)(1 << p);
            /* set read plane */
            IOPort.outb(VGA_GC_INDEX, 4);
            IOPort.outb(VGA_GC_DATA, p);
            /* set write plane */
            IOPort.outb(VGA_SEQ_INDEX, 2);
            IOPort.outb(VGA_SEQ_DATA, pmask);
        }

        private int get_fb_seg()
        {
            int seg;

            IOPort.outb(VGA_GC_INDEX, 6);
            seg = IOPort.inb(VGA_GC_DATA);
            seg >>= 2;
            seg &= 3;
            switch (seg)
            {
                case 0:
                case 1:
                    seg = 0xA0000;
                    break;
                case 2:
                    seg = 0xB0000;
                    break;
                case 3:
                    seg = 0xB8000;
                    break;
            }
            return seg;
        }
